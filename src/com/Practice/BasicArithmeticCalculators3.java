package com.Practice;

public class BasicArithmeticCalculators3 {

    public static void main(String[] args) {
        int num1 = 5 ,num2 = 7 ;

        var Result1 = num1 + num2;
        var Result2 = num1 * num2;
        float Result3 = num2 / num1;
        var Result4 = num1 - num2;
        float Result5 = num2 % num1;

        System.out.println("Addition of result: " + Result1);
        System.out.println("Multiplication of result: " + Result2);
        System.out.println("Division of result: " + Result3);
        System.out.println("Substraction of result: " + Result4);
        System.out.println("Mod of result: " + Result5);
    }

}
